package Lab;

public class Loops {

	public static void main(String[] args) {
		
		System.out.println("Loop using increment operator");
		
		
		for(int i=0;i<5;i++) {
			System.out.println("Hello"+" "+i);
		}
		
		
		System.out.println("Loop usig decrement operator");
		
		
		for(int i=5;i>0;i--) {
			System.out.println("World"+" "+i);
		}

	}

}
